package com.example.exampleapp.ui.quiz;

public class FoodItem {

    private String name;
    private int image;

    public FoodItem(String name, int image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public int getImage() {
        return image;
    }
}
